<div>
    <h3 class="flush push-bottom-sm">
        APA/CPA 8-Ball Game Rules
    </h3>
    <p>
        <strong>General Description</strong> - 8-Ball is played with a cue ball and
        normal rack of 15 object balls. The primary purpose of this
        game is for one player to pocket the solid balls numbered
        from 1 to 7 or the striped balls numbered from 9 to 15,
        and then mark and pocket the 8-ball before his opponent.
        Choice of balls to be pocketed is made by the player legally
        pocketing the first ball of the game.
    </p>
    <p>
        1. <strong>Lagging</strong> - Players lag to see who wins the first break.
        If the lagged balls make contact during the lag, re-lag. The
        winner of the lag breaks in the first game and from that
        point on the winner of each game breaks in the next game.
        Players are discouraged from using the cue ball during the
        lag. Insisting upon lagging with the cue ball is considered a
        sportsmanship violation to be reported to the League Office.
    </p>
    <p>
        2. <strong>Racking</strong> - All balls should be frozen (touching) as tightly
        as possible. Balls are racked with the front ball on the foot spot
        and the 8-ball in the center of the triangle. The breaking player
        may request and receive a rerack. The loser of the lag and/or
        the loser of any subsequent game is the person who racks.
    </p>
    <p>
        3. <strong>Breaking</strong> - To be a legal break, players must break from
        behind the head string. The head ball or the second row of
        balls must be struck first and at least four object balls must
        be driven to the rails or a ball must be pocketed. The cue
        ball may not be shot into a rail before the rack. If the rack
        is struck, but the break does not qualify as legal, the balls
        are reracked and rebroken by the same player. If the rack is
        struck, but the break does not qualify as legal and results in
        a scratch, the balls are reracked and broken by the opposite
        player. <strong>THE RACK MUST BE STRUCK BEFORE A FOUL
        CAN OCCUR.</strong> Breaking safe or soft is not allowed.
    </p>
    <p>
        The League Operator may make judgments and issue
        penalties to teams and players who are not breaking hard.
        Breaking just hard enough to comply with this rule is not a
        guarantee against penalties. Remember, break as hard as you
        can with control.
    </p>
    <p>
        4. <strong>After the Break</strong> - Various circumstances can occur upon
        completion of the break. They are:

        <ol class="alpha-list">
            <li>
                A foul on a legal break results in <em>ball-in-hand</em> behind
                the head string and the incoming player may shoot
                any ball outside the head string (see diagram of table
                in Team Manual for explanation). A ball that’s dead
                center or “out” is playable. If it is “in”, the ball is not
                playable. It is up to the opponent to call the cue ball
                “in” before it is shot. (See Team Manual for a more
                complete explanation.)
            </li>
            <li>
                No balls are pocketed and it is the other player’s turn.
            </li>
            <li>
                The 8-ball is pocketed. This is a win, unless the player
                scratches, in which case he loses.
            </li>
            <li>
                A ball is pocketed; it is still the breaker’s
                turn and he continues shooting the category of balls he
                just made.
            </li>
            <li>
                A ball of each category is pocketed (for example, the
                6-ball and the 12-ball). Now the breaker has his choice.
                He may shoot any ball except the 8-ball (which would
                be a foul); if he does not foul, anything that goes in
                counts. If he were to make one of each on his second
                shot, he would still have an <em>open table</em> and the same
                choice as after the break. If he were to miss or foul on
                his second shot, his opponent would have an <em>open
                table</em>. If the opponent then shoots and makes a ball,
                but also fouls on the shot, it is still an <em>open table</em>.
                <em>Open table</em> means a player can shoot a combination
                involving a stripe and a solid and whichever he makes,
                without committing a foul, would become his category
                for the remainder of the game.
            </li>
            <li>
                If two balls of one category and one ball of
                the other category are pocketed, it is the shooter’s
                choice just as in “e” above.
            </li>
            <li>
                Occasionally, a player mistakenly starts shooting the
                wrong category of balls. Although it is sportsmanlike
                for the sitting player to remind the shooting player that
                he is about to foul by shooting the wrong category of
                balls, it is not a requirement for him to do so. Once the
                shooter has hit the wrong category of balls, the foul has
                occurred whether the ball is pocketed or not. If the ball
                is pocketed, it is permissible, though not recommended,
                for the sitting player to allow the shooting player to
                continue pocketing the wrong category of balls until
                he feels inclined to call the foul. The shooting player
                can escape penalty by quietly realizing his error and
                returning to shoot the correct category of balls and
                legally contacting one of them before his opponent calls
                foul, or by finishing off the wrong category of balls
                and legally contacting the 8-ball prior to his opponent
                calling a foul. In other words, the sitting player must
                call the foul before the shooter returns to the correct
                category and makes legal contact or before the shooter
                pockets the remaining balls of the wrong category and
                legally contacts the 8-ball. Once a player makes legal
                contact with the 8-ball, the player assumes control of
                that category of wrongly pocketed balls and can win the
                game by pocketing the 8-ball. In addition, if the sitting
                player does not call a foul before his opponent’s turn
                ends, and subsequently contacts the wrong category
                himself, both players will assume the new category of
                balls for the remainder of the game. Before any foul has
                occurred, the shooter also may avoid penalty by asking
                the sitting player which category of balls he has. The
                sitting player must tell him the truth.
            </li>
        </ol>
    </p>
    <p>
        5. <strong>Combination Shots</strong> - Combination shots are legal,
        but striking the correct ball first is required except in the
        <em>open table</em> situation. <strong>The 8-ball is not neutral</strong>. A player is
        credited with all balls he legally pockets. When a player does
        not pocket one of his balls but pockets an opponent’s ball, he
        loses his turn. The opponent gets credit for the pocketed ball.
        No pocketed ball is ever spotted.
    </p>
    <p>
        6. <strong>Balls on the Floor</strong> - Knocking the cue ball off the playing
        surface is a foul. If the 8-ball is knocked on the floor, it is loss
        of game. <strong>Knocking any other object ball on the floor is not a
        foul</strong>. Object balls that get knocked off the playing surface will
        be spotted on the foot spot. If the foot spot is taken, the ball
        will be placed on a line directly behind the foot spot as close to
        the foot spot as possible. If two or more balls are knocked on
        the floor, they are placed in numerical order with the lowest
        numbered ball closest to the foot spot; the placed balls will
        be frozen to one another. It might occur that a player legally
        pockets a ball while simultaneously knocking some other
        ball(s) on the floor. In this situation, it is still his turn and the
        ball(s) is not spotted until he misses. If the ball on the floor is
        one of the shooter’s balls, then it is spotted when the shooter
        has pocketed all of his other balls or misses.
    </p>
    <p>
        7. <strong>Pocketed Balls</strong> - Balls must remain in a pocket to be legal.
        If a ball goes in a pocket, but bounces back onto the playing
        surface, it is not considered pocketed. If it is the
        8-ball, it is not considered either a win or loss. If it is the cue
        ball, it is not considered a scratch.
    </p>
    <p>
        <strong>Note 1</strong>: If a ball which has been hanging in a pocket for more
        than a few seconds suddenly falls in, it is to be placed back
        on the table where it was originally sitting. Once a ball has
        <em>stopped</em> all motion, it cannot move again without outside
        forces affecting it. So, if it falls in a pocket, it is to be placed
        back on the table where it was before it fell.
    </p>
    <p>
        <strong>Note 2</strong>: It occasionally happens on tables with small pockets
        that two balls become jammed in a pocket and are leaning
        over the edge of the slate to some degree. They are off the
        playing surface and are considered pocketed. Drop them in
        and resume playing the game unless the pocketing ends the
        game (8-ball or cue ball scratch when shooting the 8-ball).
    </p>
    <p>
        8. <strong>One Foot on the Floor</strong> - At least one foot must be
        on the floor at all times while shooting if a bridge is present.
        There is no foul, simply stop the shooter and hand him the
        bridge. League Management cannot guarantee the presence
        of bridges, and some Host Locations do not have them. If a
        bridge is not available, house rules prevail. <strong>Exception</strong>: Players
        shooting from a wheelchair must remain seated in their
        wheelchair while shooting.
    </p>
    <p>
        9. <strong>Marking the Table</strong> - Marking refers to a physical
        alteration in the appearance of the cloth on a pool table. No
        one is allowed to mark the cloth. For example, using chalk to
        draw a line or wetting your finger to dampen the cloth is not
        allowed. It is permissible to set a piece of chalk on the hard
        surface of the rail.
    </p>
    <p>
        10. <strong>Fouls</strong> - If any of the following fouls are committed,
        the penalty is <em>ball-in-hand</em> for the incoming player. Make
        certain you have <em>ball-in-hand</em> before you touch the cue ball.
        Confirm it with your opponent before touching the cue ball.
        <em>Ball-in-hand</em> might be new for many members and therefore
        warrants further explanation. <em>Ball-in-hand</em> means you get to
        put the cue ball anywhere on the table (with the exception of
        a scratch on the break which results in <em>ball-in-hand</em> behind
        the head string), and shoot any of your balls (or the 8-ball, if
        all of your balls have been pocketed) regardless of where that
        ball is. A player exercising his rights under the <em>ball-in-hand</em>
        rule may place the cue ball on the table anywhere he desires.
        Even after having addressed the cue ball a player may, if
        not satisfied with the placement, make further adjustments
        with his hand, cue stick or any other reasonable piece of
        equipment. A foul may be called only if the player fouls the
        cue ball while actually stroking at the cue ball, meaning a
        double hit of the cue ball (sometimes called double clutching).
        The <em>ball-in-hand</em> rule penalizes a player for an error. Without
        this rule, a person can actually benefit by accidentally or
        purposely scratching or otherwise fouling. In the unlikely
        event that a game should ever become stalemated, meaning
        that neither player wants or can make use of ball-in-hand,
        then the balls are reracked, the same player breaks and
        the innings for the stalemated game are crossed off the
        scoresheet.
    </p>
    <p>
        Only the player or the Team Captain may officially call a
        foul, although anyone may suggest to the player or the Team
        Captain that a foul should be called.
    </p>
    <p>
        These are the only fouls resulting in ball-in-hand. All other
        violations are sportsmanship violations. The ball-in-hand
        fouls are as follows:

        <ol class="alpha-list">
            <li>
                Anytime during a shot, the cue ball goes in a pocket, on
                the floor, or otherwise ends up off the playing surface.
            </li>
            <li>
                Failure to hit a correct ball first. (A player who is
                shooting stripes must hit a striped ball first.) The
                8-ball is not neutral. In general, the shooter has the
                advantage in close hit situations unless his opponent
                has asked an outside party to watch the hit. Protect
                yourself. If you think your opponent is getting ready
                to shoot a shot that could possibly be a bad hit, stop
                him from shooting and get someone to watch the shot.
                Potential bad hit situations are usually fairly obvious
                and protests and disputes over these close situations
                can almost always be avoided if someone is asked to
                watch the shot. If the outside party cannot determine
                which ball was struck first, such as a simultaneous
                hit, the call goes to the shooter. Teams involved in
                repeatedly calling bad hits without outside party
                verification may be subject to penalty points for
                disruptive unsportsmanlike behavior.
            </li>
            <li>
                Failure to hit a rail after contact. A rail must be hit
                by either the cue ball or any other ball after the cue
                ball and the object ball make contact. A pocketed ball
                counts as a rail. Even if the ball bounces back onto the
                playing surface, it is considered to have hit a rail, as the
                pocket liner is part of the rail. A sentence that should
                answer many questions is: <strong>ANY ball must go to a rail
                AFTER LEGAL contact</strong>.
            </li>
            <li>
                The object ball is frozen to a rail and the player is
                contemplating playing a safety. In order for the frozen
                ball rule to be in effect, the opponent must declare that
                the ball is frozen and the player should verify. Once
                it is agreed the ball is frozen the player must drive the
                object ball to another rail (of course, it could hit another
                ball, which in turn hits a rail), or drive the cue ball to a
                rail after it touches the object ball. If the latter method
                of safety is chosen then the player should take care that
                he quite obviously strikes the object ball first. Unlike
                simultaneous hits between object balls, if the cue ball
                strikes the rail first or appears to hit both the rail and
                ball simultaneously, then it would be a foul unless
                either the cue ball or object ball went to some other rail.
            </li>
            <li>
                It is illegal and, therefore, a foul to jump a cue ball over
                another ball by scooping it up in the air on purpose.
            </li>
            <li>
                Receiving illegal aid (coaching from person(s) other
                than the coach) during your turn at the table. It is not
                considered illegal aid to remind a player to mark the
                pocket when shooting the 8-ball, or to tell a player a
                foul has occurred. Anyone may do so.
            </li>
            <li>
                Causing even the slightest movement or altering the
                course of the cue ball, even accidentally, is a foul. Even
                dropping the chalk on the cue ball is a foul. It is not
                a foul, however, to accidentally move any other balls
                (including the 8-ball) unless, during his turn at the
                table, a player moves a ball and it in turn comes in
                contact with the cue ball. Any balls moved accidentally
                during a shot <strong>must be replaced</strong> by the opponent after
                the shot is over and all balls have stopped rolling. If
                during the course of the shot, another ball stops in
                the position previously occupied by the accidentally
                moved ball, the opponent must place the accidentally
                moved ball, in an ethical manner, as close as possible to
                its original position. If it occurs before the shot, it must
                be replaced by the opponent before the shot is taken.
                <br><br>
                <strong>Exception</strong>: If an accidentally moved ball comes in
                contact with the cue ball, creating a foul, no object ball
                will be replaced.
            </li>
            <li>
                If, during the course of a shot, the cue ball does not
                touch anything.
            </li>
            <li>
                Exercise caution when picking up or placing the cue
                ball in a ball-in-hand situation. The cue ball is always
                alive. If the cue ball, <strong>or the hand holding or moving
                it</strong>, touches another ball it is a cue ball foul and your
                opponent has ball-in-hand. Be especially careful when
                you are picking up or placing the cue ball in a tight
                spot.
            </li>
            <li>
                The player or his coach (during a coaching time-out)
                may place the cue ball in a ball-in-hand situation. The
                same rule regarding placing the cue ball applies to the
                coach as applies to the player. If the player, or coach
                fouls in the process of placing the cue ball, it will be
                ball-in-hand for the opponent. Therefore, it should be
                the player’s choice if he wishes to place the cue ball or
                allow his coach to do so.
            </li>
        </ol>
    </p>
    <p>
        11. <strong>Ways to Lose</strong>:

        <ol class="alpha-list">
            <li>
                Your opponent pockets his numerical group and legally
                pockets the 8-ball.
            </li>
            <li>
                You pocket the 8-ball out of turn or knock it on the floor.
            </li>
            <li>
                When playing the 8-ball, you pocket the 8-ball in the
                wrong pocket or fail to properly mark the pocket where
                the 8-ball went in.
            </li>
            <li>
                You foul the cue ball and then pocket the 8-ball.
            </li>
            <li>
                When playing the 8-ball, you scratch. You lose whether
                or not you pocket the 8-ball.
                <br><br>
                <strong>Note</strong>: If you are shooting at the 8-ball and miss it altogether
                without scratching, you have fouled and your opponent has
                ball-in-hand, but you don’t lose because of this foul.
            </li>
            <li>
                A game is lost if you alter the course of the
                8-ball or the cue ball in a game losing situation.
            </li>
        </ol>
    </p>
    <p>
        12. <strong>How to Win</strong> - You have won the game when all the
        balls of your numerical group have been pocketed and you
        have legally pocketed the 8-ball in a properly marked pocket
        without scratching. To properly mark the pocket, a coaster or
        some other reasonable marker (to avoid confusion, although
        it is permissible, we do not recommend marking the pocket
        with chalk) must be placed next to the pocket the 8-ball is
        intended to enter. Both teams may use the same marker.
        Only one marker should be on the table. However, if more
        than one marker is on the table, as long as you clearly mark
        the pocket where you intend to pocket the 8-ball you have
        properly marked the pocket. If the marker is already at the
        intended pocket from a previous attempt or game, it is not
        necessary for you to touch it, pick it up, or reposition it.
    </p>
    <p>
        <strong>Note</strong>: You cannot play the 8-ball while simultaneously
        playing the last ball of your category. The 8-ball must be a
        separate shot.
    </p>