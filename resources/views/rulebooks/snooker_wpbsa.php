<div>
    <h3 class="flush push-bottom-sm">
        WPBSA Snooker Game Rules
    </h3>
    <p>
        <strong>1. Description</strong>
        Snooker may be played by two or more players, either independently or as
        sides. The game can be summarised as follows:

        <ol class="alpha-list">
            <li>Each player uses the same White cue-ball and there are twenty-one object balls – fifteen Reds each valued 1, and six colours: Yellow valued 2, Green 3, Brown 4, Blue 5, Pink 6 and Black 7.</li>
            <li>Scoring strokes in a player‟s turn are made by potting Reds and colours alternately until all the Reds are off the table and then the colours in the ascending order of their value.</li>
            <li>Points awarded for scoring strokes are added to the score of the striker.</li>
            <li>Penalty points from fouls are added to the opponent‟s score.</li>
            <li>A tactic employed at any time during a frame is to leave the cue-ball behind a ball not on such that it is snookered for the next player. If a player or side is more points behind than are available from the balls left on the table, then the laying of snookers in the hope of gaining points from fouls becomes most important.</li>
            <li>The winner of a frame is the player or side:

                <ol class="bullet-list">
                    <li>making the highest score;</li>
                    <li>to whom the frame is conceded; or</li>
                    <li>to whom it is awarded under Section 3 Rule 14(d)(ii) or Section 4 Rule 2.</li>
                </ol>
            </li>
            <li>
                The winner of a game is the player or side:

                <ol class="bullet-list">
                    <li>winning most, or the required, number of frames;</li>
                    <li>making the greatest total where aggregate points are relevant; or</li>
                    <li>to whom the game is awarded under Section 4 Rule 2.</li>
                </ol>
            </li>
            <li>The winner of a match is the player or side winning most games or, where aggregate points are relevant, with the greatest total.</li>
        </ol>
    </p>
    <p>
        <strong>2. Position of Balls</strong>

        <ol class="alpha-list">
            <li>
                At the start of each frame the cue-ball is in-hand and the object balls are positioned on the table as follows:

                <ol class="bullet-list">
                    <li>
                        the Reds in the form of a tightly-packed equilateral triangle, with the
                        Red at the apex standing on the centre line of the table, above the
                        Pyramid Spot such that it will be as close to the Pink as possible
                        without touching it, and the base of the triangle nearest to, and
                        parallel with, the top cushion;
                    </li>
                    <li>
                        the six colours on the spots designated in Section 1, Rule 1(f).
                    </li>
                </ol>
            </li>
            <li>If an error in setting up the table is made, Section 3 Rule 7(c) shall apply, the frame starting as in Section 3 Rule 3(c).</li>
            <li>
                After a frame has started, a ball in play may only be cleaned by the referee upon reasonable request by the striker and:

                <ol class="bullet-list">
                    <li>
                        the position of the ball, if not spotted, shall be marked by a suitable
                        device prior to the ball being lifted for cleaning;
                    </li>
                    <li>
                        the device used to mark the position of a ball being cleaned shall be
                        regarded as and acquire the value of the ball until such time as the
                        ball has been cleaned and replaced. If any player other than the
                        striker should touch or disturb the device, the Referee shall call
                        PENALTY and the offender shall be penalised as if he were the
                        striker, without affecting the order of play. The referee shall return
                        the device or ball being cleaned to its position, if necessary, to his
                        satisfaction, even if it was picked up.
                    </li>
                </ol>
            </li>
        </ol>
    </p>
    <p>
        <strong>3. Mode of Play</strong>
        The players shall determine the order of play by lot or in any mutually agreed
        manner, the winner having the choice of which player plays first.

        <ol class="alpha-list">
            <li>
                The order of play thus determined must remain unaltered throughout the
                frame, except a player may be asked by the next player to play again
                after any foul.
            </li>
            <li>The player or side to strike first must alternate for each frame during a game.</li>
            <li>
                The first player plays from in-hand, the frame commencing when the cueball
                has been placed on the table and contacted by the tip of the cue
                either:

                <ol class="bullet-list">
                    <li>as a stroke is made; or</li>
                    <li>while addressing the cue-ball.</li>
                </ol>
            </li>
            <li>
                If a frame is started by the wrong player or side:

                <ol class="bullet-list">
                    <li>
                        it shall be restarted correctly, without penalty, if only one stroke has
                        been played and no foul has been committed since; or
                    </li>
                    <li>
                        it shall continue in the normal way if another stroke has been played,
                        or if a foul is committed after the completion of the first stroke, with
                        the correct order of starting being resumed in the following frame
                        such that one player or side will have started in three consecutive
                        frames; or
                    </li>
                    <li>
                        it shall, in the event of a stalemate being declared (See Section 3
                        Rule 16) be re-started by the correct side.
                    </li>
                </ol>
            </li>
            <li>For a stroke to be fair, none of the infringements described below in Rule 10 (Penalties) must occur.</li>
            <li>For the first stroke of each turn, until all Reds are off the table, Red or a free ball nominated as a Red is the ball on, and the value of each Red and any free ball nominated as a Red, potted in the same stroke, is scored.</li>
            <li>
                <ol class="bullet-list">
                    <li>
                        If a Red, or a free ball nominated as a Red, is potted, the same
                        player plays the next stroke and the next ball on is a colour of the
                        striker‟s choice which, if potted, is scored and the colour is then
                        spotted;
                    </li>
                    <li>
                        The break is continued by potting Reds and colours alternately until
                        all the Reds are off the table and, where applicable, a colour has
                        been played at following the potting of the last Red;
                    </li>
                    <li>
                        The colours then become on in the ascending order of their value as
                        per Section 3 Rule 1(a) and when next potted remain off the table,
                        except as provided for in Section 3 Rule 4, and the striker plays the
                        next stroke at the next colour on;
                    </li>
                    <li>
                        In the event that the striker, in a break, plays before the referee has
                        completed spotting a colour while all other balls are at rest, the value
                        of the colour shall not be scored and Section 3 Rule 10(a) (i) or
                        Section 3 Rule 10(b) (ii) shall apply as appropriate.
                    </li>
                </ol>
            </li>
            <li>
                Reds are not usually replaced on the table once pocketed or forced off
                the table regardless of the fact that a player may thus benefit from a foul.
                However, exceptions are provided for in Section 3; Rules 2(c) (ii), Rule 9,
                Rule 14(b) and (f), 15(a) and 18(c).
            </li>
            <li>
                If the striker fails to pot a ball, he must leave the table without undue
                delay. In the event that he should commit any foul before, or while leaving
                the table, he will be penalised as provided for in Section 3 Rule 10. The
                next stroke is then played from where the cue-ball comes to rest, or from
                in-hand if the cue-ball is off the table, except when the cue-ball is
                replaced in accordance with Section 3 Rule 14(e).
            </li>
            <li>If any ball enters a pocket and rebounds onto the bed of the table, it does not count as having been pocketed. The striker has no redress if this occurs because a pocket is overloaded.</li>
        </ol>
    </p>
    <p>
        <strong>4. End of Frame, Game or Match</strong>

        <ol class="alpha-list">
            <li>
                When Black is the only object ball remaining on the table, the first score
                or foul ends the frame excepting only if the following conditions both
                apply:

                <ol class="bullet-list">
                    <li>the scores are then equal; and</li>
                    <li>aggregate scores are not relevant.</li>
                </ol>
            </li>
            <li>
                When both conditions in (a) above apply:

                <ol class="bullet-list">
                    <li>the Black is spotted;</li>
                    <li>the players draw lots for choice of playing next;</li>
                    <li>the next player plays from in-hand; and</li>
                    <li>the next score or foul ends the frame.</li>
                </ol>
            </li>
            <li>
                When aggregate scores determine the winner of a game or match, and
                the aggregate scores are equal at the end of the last frame, the players in
                that frame shall follow the procedure for a re-spotted Black set out in (b)
                above.
            </li>
        </ol>
    </p>
    <p>
        <strong>5. Playing from In-hand</strong>
        To play from in-hand, the cue-ball must be struck from a position on or within the
        lines of the “D”, but it may be played in any direction.

        <ol class="alpha-list">
            <li>
                The referee will state, if asked, whether the cue-ball is properly placed
                (that is, not outside the lines of the “D”).
            </li>
            <li>
                If the tip of the cue should touch the cue-ball while positioning it, and the
                referee is satisfied that the striker was not attempting to play a stroke,
                then the cue-ball is not in play.
            </li>
        </ol>
    </p>
    <p>
        <strong>6. Hitting Two Balls Simultaneously</strong>
        Two balls, other than two Reds or a free ball and a ball on, must not be struck
        simultaneously by the first impact of the cue-ball.
    </p>
    <p>
        <strong>7. Spotting Colours</strong>
        Any colour pocketed or forced off the table shall be spotted before the next
        stroke is made, until finally potted under Section 3 Rule 3 (g) (iii).

        <ol class="alpha-list">
            <li>
                A player shall not be held responsible for any mistake by the referee in
                failing to spot correctly any ball.
            </li>
            <li>
                If a colour is spotted in error after being potted in ascending order as per
                Section 3 Rule 3 (g) (iii), it shall be removed from the table without
                penalty when the error is discovered and play shall continue from the
                resulting position.
            </li>
            <li>
                If a stroke is made with a ball or balls not correctly spotted, they will be
                considered to be correctly spotted for subsequent strokes. Any colour
                incorrectly missing from the table will be spotted:

                <ol class="bullet-list">
                    <li>without penalty when discovered if missing due to previousoversight;</li>
                    <li>subject to penalty if the striker played before the referee was able to effect the spotting.</li>
                </ol>
            </li>
            <li>
                If a colour has to be spotted and its own spot is occupied, it shall be
                placed on the highest value spot available.
            </li>
            <li>
                If there is more than one colour to be spotted and their own spots are
                occupied, the highest value ball shall take precedence in order of
                spotting.
            </li>
            <li>
                If all spots are occupied, the colour shall be placed as near its own spot
                as possible, between that spot and the nearest part of the top cushion.
            </li>
            <li>
                In the case of Pink and Black, if all spots are occupied and there is no
                available space between the relevant spot and the nearest part of the top
                cushion, the colour shall be placed as near to its own spot as possible on
                the centre line of the table below the spot.
            </li>
            <li>
                In all cases, the colour when spotted must not be touching another ball.
            </li>
            <li>
                A colour, to be properly spotted, must be placed by hand on the spot
                designated in these Rules.
            </li>
        </ol>
    </p>
    <p>
        <strong>8. Touching Ball</strong>

        <ol class="alpha-list">
            <li>
                If at the completion of a stroke the cue-ball is touching a ball or balls on,
                or that could be on, the referee shall state TOUCHING BALL and indicate
                which ball or balls on the cue-ball is touching. If the cue-ball is touching
                one or more colours after a Red (or a free ball nominated as a Red) has
                been potted, the referee shall also ask the striker to DECLARE which
                colour he is on.
            </li>
            <li>
                When a touching ball has been called, the striker must play the cue-ball
                away from that ball without moving it or it is a push stroke.
            </li>
            <li>
                Providing the striker does not cause any touching object ball to move,
                there shall be no penalty if:

                <ol class="bullet-list">
                    <li>(i) the ball is on;</li>
                    <li>(ii) the ball could be on and the striker declares he is on it; or</li>
                    <li>(iii) the ball could be on and the striker declares, and first hits, another ball that could be on.</li>
                </ol>
            </li>
            <li>
                If the cue-ball comes to rest touching or nearly touching a ball that is not
                on, the referee, if asked whether it is touching, will answer YES or NO.
                The striker must play away without disturbing it as above but must first hit
                a ball that is on.
            </li>
            <li>
                When the cue-ball is touching both a ball on and a ball not on, the referee
                shall only indicate the ball on as touching. If the striker should ask the
                referee whether the cue-ball is also touching the ball not on, he is entitled
                to be told.
            </li>
            <li>
                If the referee is satisfied that any movement of a touching ball at the
                moment of striking was not caused by the striker, he will not call a foul.
            </li>
            <li>
                If a stationary object ball, not touching the cue-ball when examined by the
                referee, is later seen to be in contact with the cue-ball before a stroke has
                been made, the balls shall be repositioned by the referee to his
                satisfaction. This also applies to a touching ball which later, when
                examined by the referee is not touching; the balls shall be repositioned by
                the referee to his satisfaction.
            </li>
        </ol>
    </p>
    <p>
        <strong>9. Ball on Edge of Pocket</strong>
        When a ball falls into a pocket without being hit by another ball, and:

        <ol class="alpha-list">
            <li>
                Being no part of any stroke in progress, it shall be replaced and any
                points previously scored shall count.
            </li>
            <li>
                If it would have been hit by any ball involved in a stroke:

                <ol class="bullet-list">
                    <li>
                        with no infringement of these Rules (including cases where an
                        infringement would have occurred but for the ball falling into a
                        pocket), all balls will be replaced and the same stroke played again,
                        or a different stroke may be played at his discretion, by the same
                        striker;
                    <li>
                    <li>
                        if a foul is committed, the striker incurs the penalty prescribed in
                        Section 3 Rule 10, all balls will be replaced and the next player has
                        the usual options after a foul.
                    </li>
                </ol>
            </li>
            <li>
                If a ball balances momentarily on the edge of a pocket and then falls in; it
                shall count as in the pocket and not be replaced.
            </li>
        </ol>
    </p>
    <p>
        <strong>10. Penalties.</strong>
        The following acts are fouls and incur a penalty of four points unless a higher one is
        indicated in paragraphs (a) to (d) below. Penalties are:

        <ol class="alpha-list">
            <li>
                value of the ball on by:
                <ol class="bullet-list">
                    <li>striking before the referee has completed the spotting of a colour taken as a free ball;</li>
                    <li>striking the cue-ball more than once during a stroke;</li>
                    <li>striking when both feet are off the floor;</li>
                    <li>playing out of turn, or playing a stroke before his opponent‟s turn has ended contrary to Section 2 Rule 6 (e);</li>
                    <li>playing improperly from in-hand, including at the opening stroke;</li>
                    <li>causing the cue-ball to miss all object balls;</li>
                    <li>causing the cue-ball to enter a pocket;</li>
                    <li>causing the cue-ball to be snookered behind a free ball, except as provided for in Section 3 Rule 12(b) (ii);</li>
                    <li>playing a jump shot;</li>
                    <li>playing with a non-standard cue; or</li>
                    <li>conferring with a partner contrary to Section 3 Rule 17(e);</li>
                </ol>
            </li>
            <li>
                value of the ball on or ball concerned, whichever is higher, by:

                <ol class="bullet-list">
                    <li>striking when any ball is not at rest;</li>
                    <li>striking before the referee has completed the spotting of a colour that is not a free ball;</li>
                    <li>causing a ball not on to enter a pocket;</li>
                    <li>causing the cue-ball to first hit a ball not on;</li>
                    <li>making a push stroke;</li>
                    <li>touching a ball or ball marker in play; or</li>
                    <li>causing a ball to be forced off the table;</li>
                </ol>
            </li>
            <li>
                value of the ball on or higher value of the two balls concerned by causing
                the cue-ball to first hit simultaneously two balls, other than two Reds
                (when Red is the ball on) or a free ball and a ball on;
            </li>
            <li>
                seven points if the striker:

                <ol class="bullet-list">
                    <li>uses a ball off the table for any purpose;</li>
                    <li>uses any object to measure gaps or distance;</li>
                    <li>touches any ball during consultation as described in Section 3, Rule 14(g)</li>
                    <li>plays at Reds, or a free ball followed by a Red, in successive strokes;</li>
                    <li>uses any ball other than White as the cue-ball for any stroke after the frame has started;</li>
                    <li>fails to declare which ball he is on when snookered or when requested to do so by the referee; or</li>
                    <li>after potting a Red (or free ball nominated as a Red), commits a foul before a colour has been nominated.</li>
                </ol>
            </li>
        </ol>
    </p>
    <p>
        <strong>11. Fouls</strong>
        If a foul is committed, the referee shall immediately state FOUL.

        <ol class="alpha-list">
            <li>
                If the striker has not made a stroke, his turn ends immediately and the
                referee shall announce the penalty.
            </li>
            <li>
                If a stroke has been made, the referee will wait until completion of the
                stroke before announcing the penalty.
            </li>
            <li>
                If a foul is neither awarded by the referee, nor successfully claimed by the
                non-striker before the next stroke is made, it is condoned.
            </li>
            <li>
                Any colour not correctly spotted shall remain where positioned except that
                if off the table it shall be correctly spotted.
            </li>
            <li>
                All points scored in a break before a foul is awarded are allowed but the
                striker shall not score any points for any ball pocketed in a stroke called
                foul.
            </li>
            <li>
                The next stroke is played from where the cue-ball comes to rest or, if the
                cue-ball is off the table, from in-hand.
            </li>
            <li>
                If more than one foul is committed in the same stroke, the highest value
                penalty shall be incurred.
            </li>
            <li>
                The player who committed the foul:

                <ol class="bullet-list">
                    <li>incurs the penalty prescribed in Rule 10 (Penalties); and</li>
                    <li>has to play the next stroke if requested by the next player.</li>
                </ol>
            </li>
            <li>
                If a striker, when snookered or hampered in any way, fouls any ball
                including the cueball while preparing to play a stroke, if requested by his
                opponent to play again, the opponent shall have the choice as to whether the
                ball on shall be the same as it was prior to the infringement, namely:

                <ol class="bullet-list">
                    <li>Any Red, where Red was the ball on;</li>
                    <li>The colour on where all the Reds were off the table; or either</li>
                    <li>A colour of the striker‟s choice, where the ball on was a colour after a                     Red had been potted; or</li>
                    <li>The option of playing the next Red or Yellow when there are no Reds remaining.</li>
                </ol>

                Any ball(s) moved shall be replaced to their original position(s) by the referee if
                requested by the non-offender.
            </li>
        </ol>
    </p>
    <p>
        <strong>12. Snookered After a Foul</strong>
        After a foul, if the cue-ball is snookered (see Section 2, Rule 17), the referee
        shall state FREE BALL.

        <ol class="alpha-list">
            <li>
                If the player next in turn elects to play the next stroke:

                <ol class="bullet-list">
                    <li>he may nominate any ball as the ball on, but a free ball cannot be the ball on;</li>
                    <li>any nominated ball shall be regarded as, and acquire the value of, the ball on except that, if potted, it shall then be spotted.</li>
                </ol>
            </li>
            <li>
                It is a foul if the cue-ball should:

                <ol class="bullet-list">
                    <li>fail to hit the nominated ball first, or first simultaneously with the ball on; or</li>
                    <li>be snookered on all Reds, or the ball on, by the free ball thus nominated, except when the Pink and Black are the only object balls remaining on the table.</li>
                </ol>
            </li>
            <li>
                If the free ball is potted, it is spotted and the value of the ball on is scored.
            </li>
            <li>
                If a ball on is potted, after the cue-ball struck the nominated ball first, or
                simultaneously with a ball on, the ball on is scored and remains off the
                table.
            </li>
            <li>
                If both the nominated ball and a ball on are potted, only the ball on is
                scored unless it was a Red, when each ball potted is scored. The free ball
                is then spotted and the ball on remains off the table.
            </li>
            <li>
                If the offender is asked to play again, the free ball call becomes void.
            </li>
        </ol>
    </p>
    <p>
        <strong>13. Play Again</strong>
        Once a player has requested an opponent to play again after a foul or
        requested the replacement of ball(s) after a FOUL AND A MISS, such request
        cannot be withdrawn. The offender, having been asked to play again, is
        entitled to:

        <ol class="alpha-list">
            <li>
                change his mind as to:

                <ol class="bullet-list">
                    <li>which stroke he will play; and</li>
                    <li>which ball on he will attempt to hit;</li>
                </ol>
            </li>
            <li>
                score points for any ball or balls he may pot.
            </li>
        </ol>
    </p>
    <p>
        <strong>14. Foul and a Miss</strong>

        <ol class="alpha-list">
            <li>
                The striker shall, to the best of his ability, endeavour to hit the ball on or a
                ball that could be on after a Red has been potted. If the referee considers
                the Rule infringed, he shall call FOUL AND A MISS unless:

                <ol class="bullet-list">
                    <li>any player needed penalty points before, or as a result of, the stroke being played;</li>
                    <li>before or after the stroke, the points available on the table are equal to the points difference excluding the value of the re-spotted black; and the referee is satisfied that the miss was not intentional.</li>
                    <li>a situation exists where it is impossible to hit the ball on.</li>
                </ol>

                In the latter case it must be assumed the striker is attempting to hit the
                ball on provided that he plays, directly or indirectly, at the ball on with
                sufficient strength, in the referee‟s opinion, to have reached the ball on
                but for the obstructing ball or balls.
            </li>
            <li>
                After a FOUL AND A MISS has been called, the next player may request
                the offender to play again from the position left, or at his discretion, from
                the original position, with all balls being replaced, in which latter case the
                ball on shall be the same as it was prior to the last stroke made, namely:

                <ol class="bullet-list">
                    <li>Any Red, where Red was the ball on;</li>
                    <li>the colour on, where all Reds were off the table; or</li>
                    <li>a colour of the striker’s choice, where the ball on was a colour after a Red had been potted.</li>
                </ol>
            </li>
            <li>
                If the striker, in making a stroke, fails to first hit a ball on when there is a
                clear path in a straight line from the cue-ball to any part of any ball that is
                or could be on, the referee shall always call FOUL AND A MISS unless as
                described under paragraph (a)(i) and (a)(ii).
            </li>
            <li>
                After a miss has been called under paragraph (c) above when there was
                a clear path in a straight line from the cue-ball to a ball that was on or
                could have been on, such that central, full ball, contact was available (in
                the case of Reds, this to be taken as a full diameter of any Red that is not
                obstructed by a colour), then:

                <ol class="bullet-list">
                    <li>a further failure to first hit a ball on in making a stroke from the original position shall be called as a FOUL AND A MISS regardless of the difference in scores;</li>
                    <li>if asked to play again from the original position, the offender shall be warned by the referee that a third failure will result in the frame being awarded to his opponent; and</li>
                    <li>if asked to play from the position left, the FOUL AND A MISS situation starts again.</li>
                </ol>
            </li>
            <li>
                After the cue-ball has been replaced under this Rule, and the striker fouls
                any ball, including the cue-ball while preparing to play a stroke, a miss will
                not be called if a stroke has not been played. In this case the appropriate
                penalty will be imposed. The next player may then elect to play himself or
                ask the offender to play again from the position left or the original
                position. If being asked to play again, the ball on shall be the same as
                prior to the last stroke made, namely:

                <ol class="bullet-list">
                    <li>any Red, where Red was the ball on;</li>
                    <li>the colour on, where all Reds were off the table; or</li>
                    <li>a colour of the striker’s choice, where the ball on was a colour after a Red had been potted;</li>
                </ol>
                If the above situation arises during a sequence of miss calls as described
                under paragraph (d), any warning concerning the possible awarding of
                the frame to his opponent shall remain in effect.
            </li>
            <li>
                When any ball is being replaced after a miss, both the offender and the
                next player will be consulted as to its position, after which the referee‟s
                decision shall be final.
            </li>
            <li>
                During such consultation, if either player should touch any ball in play, he
                shall be penalised as if he were the striker, without affecting the order of
                play. The ball touched shall be replaced by the referee, to his satisfaction,
                even if it was picked up, Section 3. 10 (d) (iii).
            </li>
        </ol>
    </p>
    <p>
        <strong>15. Ball Moved by Other than Striker</strong>
        If a ball, stationary or moving, is disturbed other than by the striker, it shall be
        re-positioned by the referee to the place he judges the ball was, or would have
        finished, without penalty.

        <ol class="alpha-list">
            <li>
                This Rule shall include cases where another occurrence or person,
                causes the striker to move a ball, but will not apply in cases where a ball
                moves due to any defect in the table surface, except in the case where a
                spotted ball moves before the next stroke has been made.
            </li>
            <li>
                No player shall be penalised for any disturbance of balls by the referee
            </li>
        </ol>
    </p>
    <p>
        <strong>16. Stalemate</strong>
        If the referee thinks a position of stalemate exists, or is being approached, he
        shall offer the players the immediate option of re-starting the frame. If any
        player objects, the referee shall allow play to continue with the proviso that the
        situation must change within a stated period, usually after three more strokes
        to each side but at the referee‟s discretion. If the situation remains basically
        unchanged after the stated period has expired, the referee shall nullify all
        scores and re-set all balls as for the start of a frame. The same player shall
        again make the opening stroke, subject to Section 3 Rule 3(d) (iii), with the
        same established order of play being maintained.
    </p>
    <p>
        <strong>17. Four-handed Snooker</strong>

        <ol class="alpha-list">
            <li>
                In a four-handed game each side shall open alternate frames and the
                order of play shall be determined at the start of each frame and, when so
                determined, must be maintained throughout that frame.
            </li>
            <li>
                Players may change the order of play at the start of each new frame.
            </li>
            <li>
                If a foul is committed and a request to play again is made, the player who
                committed the foul plays the next stroke and the order of play is
                unchanged. If the foul was called for playing out of turn, the offender‟s
                partner will lose a turn, whether or not the offender is asked to play again.
            </li>
            <li>
                When a frame ends in a tie Section 3 Rule 4 applies. If a re-spotted Black
                is necessary the pair who play the first stroke have the choice of which
                player will make that stroke. The order of play must then continue as in
                the frame.
            </li>
            <li>
                Partners may confer during a frame but not whilst one is the striker and
                has approached the table until the break has ended with a non-scoring
                stroke or foul.
            </li>
        </ol>
    </p>
    <p>
        <strong>18. Use of Ancillary Equipment</strong>
        It is the responsibility of the striker to both place and remove any equipment he
        may use at the table.

        <ol class="alpha-list">
            <li>
                The striker is responsible for all items including, but not limited to, rests
                and extensions that he brings to the table, whether owned by him or
                borrowed (except from the referee), and he will be penalised for any fouls
                made by him when using this equipment.
            </li>
            <li>
                Equipment normally found at the table which has been provided by
                another party including the referee is not the responsibility of the striker. If
                this equipment should prove to be faulty and thereby cause the striker to
                touch a ball or balls, no foul will be called.
            </li>
            <li>
                The referee will, if necessary, reposition any balls in accordance with
                Section 3 Rule 15 above and the striker, if in a break, will be allowed to
                continue without penalty.
            </li>
        </ol>
    </p>
    <p>
        <strong>19. Interpretation</strong>

        <ol class="alpha-list">
            <li>
                Throughout these Rules and Definitions, words implying the masculine
                gender shall equally apply to and include the female gender.
            </li>
            <li>
                Circumstances may necessitate adjustment in how Rules are applied for
                persons with disabilities. In particular and for example:

                <ol class="bullet-list">
                    <li>Section 3 Rule 10 (a) (iii) cannot be applied to players in wheelchairs; and</li>
                    <li>a player, upon request to the referee, shall be told the colour of a ball if he is unable to differentiate between colours as, for example, red and green.</li>
                </ol>
            </li>
            <li>
                When there is no referee, such as in a social game, the opposing player
                or side will be regarded as such for the purpose of these Rules.
            </li>
        </ol>
    </p>
