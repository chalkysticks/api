<div>
    <h3 class="flush push-bottom-sm">
        UPA 10-Ball Game Rules
    </h3>
    <p>
        <strong>OBJECT OF THE GAME</strong>
        The object of 10-ball is to win by legally pocketing the 10-ball into the “Called Pocket.”
        The cue ball must strike the lowest numbered ball first for a legal hit to occur. After the lowest ball is struck first, either the cue ball or any numbered ball may hit the 10-ball into any pocket for the win (call shot). If the shooting player pockets the lowest numbered ball, the shooting player continues his/her inning. If no ball is pocketed, either the cue ball or any numbered ball must touch a rail after the cue ball contacts the lowest numbered ball for the shot to be legal.
    </p>
    <p>
        <i>NOTE:</i> If the 10-ball is pocketed illegally or without being “called,” then it is to be spotted on the foot spot (see 7.1, Cue Ball Fouls).
    </p>
    <p>
        <strong>2.0 LAG FOR BREAK</strong>
        The player with the lowest official UPA Speed (Rating) shall break first with an alternating break format taking place thereafter. In the event that two players with equal Speeds play, a “Lag for Break” shall determine who breaks first with an alternating break format taking place thereafter. Winner of lag is the player whose ball is closer to head of the rail.
    </p>
    <p>
        <strong>2.1 How to Rack</strong>
        The balls are racked in a triangle with the 1-ball on the foot spot at the top of the triangle, the 10-ball in the center of the triangle, with the 2-ball & 3-ball placed randomly at the bottom corners, and the rest of the balls randomly dispersed throughout the rack.
    </p>
    <p>
        <i>NOTE:</i> The UPA acknowledges that tables may have flaws that do not allow for acceptable racks directly upon the foot spot. In these cases, players are allowed to rack within a dime’s radius of the direct foot spot to achieve a desired rack.
    </p>
    <p>
        <i>NOTE:</i> Touching or positioning of balls after the rack has been removed is never permitted. Player must re-rack if this occurs (see 8.5, Ball Tapping).
    </p>
    <p>
        <strong>2.2 Rack Your Own</strong>
        When there is no official available, each breaking player shall be responsible for providing himself/herself a legal and solid rack.
    </p>
    <p>
        <strong>3.0 LEGAL BREAK SHOT</strong>
        For the break shot to be legal, the breaker (with the base of the cue ball placed anywhere behind the head string) must either pocket an object ball or drive at least three (3) object balls to one or more rails. If the breaker fails to make the legal break requirement, the balls will be re-racked and the opponent shall have the option of breaking, or requesting the offending player to break again.
    </p>
    <p>
        <i>NOTE:</i> If the cue ball is touched by the cue tip and does not meet the legal break requirement, it is considered an “illegal break.”
    </p>
    <p>
        A Legal Break with a Foul committed:

        <ol class="alpha-list">
            <li>
                If the breaker makes a legal break, however, commits a foul on the legal break the game is to continue with the opponent having ball in hand.
            </li>
        </ol>

        Break fouls include the following:

        <ol class="alpha-list">
            <li>
                If the 1-ball was not struck first.
            </li>
            <li>
                If the cue ball is pocketed or driven off the table.
            </li>
            <li>
                If any numbered ball leaves the table or comes to rest on top of a rail.
            </li>
        </ol>
    </p>
    <p>
        <i>NOTE:</i> A foul on a legal break counts toward the three fouls in the “Three Consecutive Fouls” loss of game rule (see 8.2).
    </p>
    <p>
        <strong>4.0 10-BALL POCKETED ON THE BREAK</strong>
        Legally pocketing the 10-ball on the break wins the game for the breaker.
    </p>
    <p>
        <strong>5.0 PUSH OUT</strong>
        Only on the shot immediately following the break, may the shooting player elect to call a “Push.” On a “Push Out,” the shooter is required to hit the cue ball with the tip of the cue, but the cue ball is not required to touch another ball or a rail. Therefore, the rules “BAD HIT,” (see 7.3), and “NO RAIL,” (see 7.4) under “BALL IN HAND FOUL PENALTIES,” (see 7.0) do not apply, but all other foul rules are still in effect. The shooting player must declare his intention to push by saying “Push” or “Push Out” either to his opponent or to the referee, or the shot is considered a normal shot. Any ball pocketed on a “Push” stays down except the 10-ball, which would be spotted on the foot spot immediately following the “Push.” Following a legal “Push” shot, the incoming player has the option to take the shot from the new position(s) or to pass the shot back to the player who “Pushed Out.” No matter who shoots next, on the shot immediately following the “Push Out,” all “BALL IN HAND FOULS PENALTIES” (see 7.0) now apply, and the normal course of play continues.
    </p>
    <p>
        <strong>6.0 CONTINUING PLAY – CALL POCKET</strong>
        On the shot immediately following a legal break and/or a Push Out (if a Push Out occurred), the shooting player must contact the lowest numbered ball on the table or it is a foul. Unlike 9-Ball, 10-Ball IS a “Call Pocket” game and the shooter is encouraged to specify all balls along with their intended pocket. However, obvious balls and their respective pockets do not have to be specified. Any bank shot (object ball to rail), kick shot (rail(s) to object ball), or combinations (2 or more balls) must be called to their designated pocket, or they are considered a miss. When a player successfully pockets the designated object balls, they continue their inning until either a miss, foul, or win occurs.
        Call pocket notes:

        <ol class="alpha-list">
            <li>If the shooter pockets the called ball in an unintended pocket the opponent has the option to accept the table in the current position, or require the shooter to continue (Pass).</li>
            <li>When the shooter pockets an uncalled ball, the opponent has the option to accept the table in the current position, or require the shooter to continue (Pass).</li>
            <li>Any ball pocketed remains pocketed (not including the 10-ball).</li>
            <li>It is never necessary to specify details such as the number of banks, kisses, caroms, rails, etc.</li>
            <li>The break shot is never considered a “called shot.”</li>
            <li>A ball must hit a rail, or go into a pocket after contact with the cue ball (see 7.4, No Rail).</li>
        </ol>
    </p>
    <p>
        <strong>7.0 BALL IN HAND FOUL PENALTIES</strong>
        When a player commits a ball in hand foul, he/she must relinquish his/her turn at the table. The incoming player may now place the cue ball anywhere on the table to start his/her inning. If a player commits more than one foul on one shot, only one foul will be called. A player must make sure he/she has ball in hand before touching the cue ball.
    </p>
    <p>
        <strong>7.1 Cue Ball Fouls Only</strong>

        <ol class="alpha-list">
            <li>Touching the cue ball: Touching or causing even the slightest movement of the cue ball (other than a normal shot), even accidentally, is a foul. However, a player may use the ferrule or shaft of his/her cue to line up his/her cue ball when a “cue ball in hand” is in play. Using the tip is a foul and ball in hand will be given to the other player.</li>
            <li>Touching a moving object ball: Touching a moving object ball or allowing a moving ball to hit a foreign object is a ball in hand foul. If the accidental movement of a ball(s) results in the disturbed ball(s) being struck by any moving balls in play, it results in a ball in hand foul.</li>
            <li>Touching a still object ball: Any still object ball moved can only be moved back to its original position with the permission of the opponent. However the opponent may exercise the option of keeping disturbed ball(s) in new position if they so choose. Only after receiving consent from the opponent, the player who has committed the error may move the disturbed object ball(s) back to original position. If the player who has committed the infraction touches any of the disturbed balls without consent of the opponent, it will result in a loss of turn with ball in hand to the opponent.</li>
        </ol>
    </p>
    <p>
        <i>NOTE:</i> If the 10-ball was pocketed by a player’s hand or cue, it is a ball in hand foul to the opponent, and the opponent has the option of placing the 10-ball back to its original position or having it placed on the foot spot. Opponent shall continue with ball in hand.
    </p>
    <p>
        <strong>7.2 Scratch</strong>
        Pocketing the cue ball or driving it off the table is a ball in hand foul.
    </p>
    <p>
        <strong>7.3 Bad Hit</strong>
        If the first object ball contacted by the cue ball is not the lowest numbered ball in the remaining order it is a ball in hand foul.
    </p>
    <p>
        <strong>7.4 No Rail</strong>
        If after the cue ball strikes a legal ball and neither the cue ball nor any other ball hits a rail or is pocketed, it is a ball in hand foul. A “Frozen” (touching) object ball to the rail does not meet this requirement by virtue of it not “hitting” a rail.
    </p>
    <p>
        <i>NOTE:</i> Players are encouraged to mutually acknowledge when the object ball is “Frozen” (touching) to the rail.
    </p>
    <p>
        <strong>7.5 Balls Off the Table</strong>
        Causing any ball to come to rest off the playing surface is a foul and any such ball(s) are pocketed. This includes any accidental movement of a ball which results in a ball falling into a pocket. The ball accidentally pocketed is not brought back into play, and the incoming player has cue ball in hand.
        If a player knocks a ball off the table and the ball returns to the playing surface after hitting a person or an object, it is a foul (the ball remains on surface). If no object or person was contacted, then normal rules of play apply once the ball returns to the playing surface.
    </p>
    <p>
        <i>NOTE:</i> If a player removes the 10-ball from the playing surface, it results as a foul and the 10-ball is placed on the foot spot.
    </p>
    <p>
        <strong>7.6 Foot on the Floor</strong>
        Failure to have at least one foot on the floor at the moment the cue tip strikes the cue ball is a ball in hand foul.
    </p>
    <p>
        <strong>7.7 Jump Shot</strong>
        Any miscue on a jump shot is a ball in hand foul. A legal jump shot must be executed by stroking down through the cue ball (no scooping or miscues).
    </p>
    <p>
        <strong>7.8 Moving Ball</strong>
        Shooting while any ball is moving or spinning is a foul.
    </p>
    <p>
        <strong>7.9 Double Hit</strong>
        If the cue tip strikes the cue ball twice on the same stroke, it is a ball in hand foul. In order to avoid a double hit, the cue ball must be struck at a minimum of a 45° angle whenever in contact with or riskily close (1/2 inch or closer) to the intended object ball.
    </p>
    <p>
        <i>NOTE:</i> Calling a referee to watch “the hit” is always preferable. The referee shall assume that a foul has been committed if the 45° rule was not utilized by the shooter.
    </p>
    <p>
        <strong>7.10 Head String</strong>
        The base of the cue ball must be inside the head string on the break or it is a ball in hand foul.
    </p>
    <p>
        <strong>7.11 Ball in Hand Placement</strong>
        To touch an object ball, in any way, while placing the cue ball is a ball in hand foul.
    </p>
    <p>
        <strong>7.12 Interference</strong>
        While the shooting player is at the table, the non-shooting player, as well as their teammates, cannot disturb, make noises, move around, cause distraction (sharking) in any way. All players must conduct themselves in a respectful manner or a manner consistent with that of a professional, or it may result in an “Official Warning” by a UPA representative or tournament official followed by the calling of a foul (ball in hand) for interference.
    </p>
    <p>
        <i>NOTE:</i> During amateur league play it is understood that the match is between the two players and teammates are not permitted to instruct unless called upon by the shooting player for a Rules Clarification or Time Out (see 8.6, Coaching Assistance).
    </p>
    <p>
        <strong>7.13 Marking the Table</strong>
        Marking the table in any way, which could provide a player with an advantage in executing a shot, is a foul, unless the mark is removed to the satisfaction of the opponent or referee prior to shooting.
    </p>
    <p>
        <strong>7.14 Playing Out of Turn</strong>
        If/when a player shoots out of turn and it is brought to the attention of the offending shooter, the rightful player is to return to the table and continue without any penalties/fouls. It is the responsibility of both players to assume control of the table on his/her proper inning.
    </p>
    <p>
        <strong>8.0 LOSS OF GAME</strong>
    </p>
    <p>
        <strong>8.1 Opponent Wins</strong>
        The opponent legally pockets the 10-ball.
    </p>
    <p>
        <strong>8.2 Three Consecutive Fouls</strong>
        If a shooter commits a foul three times in a row without making an intervening legal shot, the result is a loss of game. The three consecutive fouls must occur in one game; fouls do not carry over to next game. After the 2nd foul has been committed, the opponent must verbally warn the shooter that they have committed their 2nd foul and that a 3rd foul will result in a loss of game.
    </p>
    <p>
        <i>NOTE:</i> If the shooter is not warned after the 2nd foul and commits a 3rd, it will not be a loss of game. Instead the opponent must again warn the shooter that the next foul committed will result in a loss of game.
    </p>
    <p>
        <strong>8.3 Conceding a Game</strong>
        Concession of a game or games in tournament play is never encouraged. The shooting player must finish his/her inning or the result shall be a loss of game(s) for the conceding player.
    </p>
    <p>
        <strong>8.4 Concession of a Match</strong>
        Unscrewing any cues during the last game (or while the shooting player is on the hill), putting on a jacket, or undertaking any other actions which would indicate that the match is over, is considered a forfeiture of the match (consult UPA representative or tournament official).
    </p>
    <p>
        <strong>8.5 Ball Tapping</strong>
        Tapping balls is not permitted. After an “Official Warning” by a UPA representative or tournament official has been granted to the offender, the penalty for ball tapping shall be the loss of the current game. Only tournament officials may tap in balls when warranted.
    </p>
    <p>
        <strong>8.6 Coaching Assistance</strong>
        During their inning and only once per game, only the shooting player is allotted a “Time Out” that shall last no more than a two (2) minute period. During this time the player may receive instruction from an available teammate. Otherwise a player who receives advice from respective teammates shall grant a ball in hand foul to the opponent. And on the second (2nd) infraction shall grant the current game in favor of the opponent by a forfeit.
        Calling a Time Out: When the shooting player calls a “Time Out” they are to select an available team member to immediately assist them without conferring with other parties. The coaching teammate is to arrive at the table and aid the shooting player with nothing in hand or it shall result in a ball in hand foul to the opponent.
    </p>
    <p>
        <strong>9.0 GENERAL POOL RULES</strong>
    </p>
    <p>
        <strong>9.1 Forfeits</strong>
        Teams are first allotted fifteen (15) minutes to begin league night activities, from the scheduled start time; otherwise the offending team’s first match will be forfeited.
        Teams/players are also allotted fifteen (15) minutes to begin their match after the end of the previous match; otherwise the offending team/player’s match will be considered forfeit.
    </p>
    <p>
        <i>NOTE:</i> Once a match is scheduled to start and a fifteen (15) minute count is desired, the Team Captain is to notify a tournament official, or UPA representative for an official count.
    </p>
    <p>
        <strong>9.2 Shot Clock</strong>
        When a shot clock is utilized for tournament play it shall be used for both players competing and in the following manner:
        Each shooting player is allotted one (1) minute for each shot, or a “ball in hand” foul shall be granted to the opponent. The shot clock is to be started once all balls come to rest. The time keeper shall call out “10-seconds” once the fifty (50) second mark has been reached, unless the shooter is down on the shot in preparation to shoot. Once one (1) minute has been reached on the shot clock, the shooter must either be stroking or have shot. If the shooter rises (gets off the shot) without execution after the one (1) minute mark has been reached, it shall result in a “ball in hand” foul to the opponent.
        Shot Clock implementation and Time Keepers are at the sole discretion of the UPA, its representative or tournament officials.
    </p>
    <p>
        <strong>9.3 Split Hits</strong>
        If the cue ball strikes a legal object ball and a non-legal object ball at about the same instant, and it cannot be clearly determined which ball was hit first, the judgment will go in favor of the shooter.
    </p>
    <p>
        <strong>9.4 Ball Rebounds from Pocket</strong>
        Balls must remain in a pocket to count as pocketed. If a ball goes into a pocket and bounces back on to the playing surface, it is not considered pocketed. If it is the 10-ball, it is not a win. If it is the cue ball, it is not a scratch. Clearing pockets which are full or nearly full of balls is the responsibility of the shooting player.
    </p>
    <p>
        <strong>9.5 Hanging Ball</strong>
        If an object ball hangs in a pocket, the ball is considered to be pocketed if it drops in 5 seconds or less after coming to complete rest by the hole. If a hanging ball drops in the pocket after being at rest for 5 seconds or more, the ball is returned to the original position on the edge and the incoming player may begin his/her inning. Both players will have the opportunity to argue their case. The referee’s decision is final.
    </p>
    <p>
        <strong>9.6 Suspended Balls</strong>
        If one or more balls become suspended in a pocket beyond the edge of the slate because it is partially supported by other pocketed balls, it is considered pocketed if the removal of the supporting ball(s) would cause the supported/suspended ball(s) to fall into the pocket. Tournament officials are the sole judges of whether this rule applies to any situation.
    </p>
    <p>
        <strong>9.7 Settling Into Place</strong>
        A ball may settle slightly after it appears to have stopped, possibly due to slight imperfections in the cloth or table slate. Unless this causes a ball to fall into a pocket, it is considered a normal hazard of play, and will not be moved back. If a ball falls into a pocket as a result of such settling, it is replaced as close as possible to its original position on the lip of the pocket. If a ball falls into a pocket during or just prior to a shot and it has an effect on the shot, the referee will restore the ball to its original position and the shot will be replayed. Players are not penalized for shooting while a ball is settling.
    </p>
    <p>
        <strong>9.8 Jump Shots</strong>
        It is legal to cause the cue ball to leave the surface of the table by elevating the butt of the cue and, with a downward stroke, forcing the cue ball to rise off the playing surface. For the shot to be legal only the cue tip may touch the cue ball – the shot must not be “scooped” by the ferrule or shaft. Any miscue on a jump shot is a ball in hand foul. A legal jump cue must be at least 40 inches in length and constructed in typical cue fashion.
    </p>
    <p>
        <i>NOTE:</i> Standard jump cues are accepted including phenolic tips.  However, cues that are not typical in appearance must be accepted and approved by the UPA.
    </p>
    <p>
        <strong>9.9 Use of Equipment </strong>
        All equipment that is generally accepted throughout the industry is permitted. However, using any equipment in a non-customary manner is never allowed and constitutes a foul; out of play balls may not be used to measure gaps or spaces of any kind. It is the responsibility of the shooting player to know what the intended use of each piece of equipment is: the bridge, jump cues, extensions, etc.
    </p>
    <p>
        <i>NOTE:</i> The use of headphones and other music devices (including Bluetooth) are not permissible during league play as they are considered an unfair advantage to the player.
    </p>
    <p>
        <strong>10.0 TOURNAMENT DIRECTOR/REFEREES</strong>
    </p>
    <p>
        <strong>10.1 Player Responsibility</strong>
        It is the responsibility of each member to be aware of all rules, regulations, and schedules relating to his/her competition. Tournament officials will make every reasonable effort to make the information readily available to all players; however, the ultimate responsibility rests with each individual player. There is no recourse if a player does not obtain correct or complete information.
    </p>
    <p>
        <i>NOTE:</i> Players may always call for rules clarification during league play. This is not considered a “Time Out.”
    </p>
    <p>
        <strong>10.2 Referees</strong>
        The League Operator (or his/her assistants) will perform the duties of a referee in the event that referees are busy or not utilized. If the Tournament Director, his/her assistants or a referee is not to be found within a reasonable time frame, a spectator may sub as an official referee when agreed upon by both players and accordance with UPA rules.
    </p>
    <p>
        <strong>10.3 Playing Without a Referee</strong>
        When a referee or tournament official is not available, the players in the match will be responsible for racking balls, watching/calling fouls (including on themselves), and insuring adherence to UPA rules of competition. Both players may agree on an audience member (familiar with UPA rules) to stand in and perform any duty of a tournament official.
    </p>
    <p>
        <strong>10.4 Questionable Shot</strong>
        If there is a shot that could be a questionable hit or foul, the seated player is responsible for calling for a tournament official or agreed upon third (3rd) party to watch the hit before the opponent shoots. Once notified, the player at the table must then wait for an “official” to watch the shot. Likewise, if a player is uncertain whether some rule has been broken, he/she is responsible for seeking immediate clarification from league officials or the rulebook before play continues. After play continues, it is unlikely that a problem can be remedied.
    </p>
    <p>
        <i>NOTE:</i> If a tournament official or third (3rd) party was not utilized, “the call” shall be left to the shooter without further discussion.
    </p>
    <p>
        <strong>10.5 Scoring</strong>
        Players are encouraged to score their own matches to ensure accuracy, however a third (3rd) party may be permitted by the League Operator. The scorer is charged to accurately record what actually took place on the pool table and may clarify with the shooter what the intention was whenever needed. I.E. If it is believed a player is purposely calling a pocket, however actually plays a safe, then a safety (S) shall be recorded. Likewise if a player calls a “safety” however fouls, then a foul (F) is recorded. A player always has the right to consult the Scorecard with the scorekeeper. Any disagreements shall be determined by the League Operator or an official UPA representative.
    </p>
    <p>
        <strong>10.6 Advice vs. Rules Clarification</strong>
        The referee must NEVER give advice nor offer an opinion on points of play. Only when asked by either player for clarification of a rule will the referee then explain that specific rule to the best of his/her ability. Any incorrect statement made by the referee will not protect a player from enforcement of the actual rule. When asked, the referee must tell either player the score, whether the cue ball is frozen to an object ball or rail, etc. If the referee sees that a foul is about to be committed by either player, he must say nothing until after the foul, since any warning before the foul would constitute “advice” from the referee.
    </p>
    <p>
        <strong>10.7 Prompting Official Warnings</strong>
        If either player has the opinion that the referee is failing to issue a mandatory “Official Warning,” he/she may remind the referee that such a warning is necessary.
    </p>
    <p>
        <strong>10.8 Calling Fouls</strong>
        The referee will call all fouls as soon as they occur and will inform the incoming player that he/she has ball in hand (see 10.3, Playing Without a Referee).
    </p>
    <p>
        <strong>10.9 Protesting Fouls</strong>
        If a player believes that the referee has failed to call a foul, he must protest to the referee before his/her opponent takes his/her next shot. If the player fails to do so, the foul is considered not to have occurred.
    </p>
    <p>
        <strong>10.10 Restoring Position</strong>
        When it becomes necessary, the referee will restore disturbed balls to their original positions to the best of their ability. The referee may solicit information for this purpose if he/she is not sure of original positions. If the balls were disturbed by a player in the match, his/her opponent has the option of preventing restoration. If the balls were disturbed by someone else, then it is a mandatory for the referee to restore the balls. In this case, the referee may instruct the shooter to replay the shot after restoration, if the outside interference had an effect on the outcome of the shot. If not, the referee will instruct the shooter to continue play after restoration.
    </p>
    <p>
        <strong>10.11 Verification</strong>
        The referee may use any means to gather needed information to make a decision concerning a disputed play or game situation.
    </p>
    <p>
        <strong>10.12 Replay of Game</strong>
        A replay of game is only warranted under the following unique circumstance;
        • Table Failure; torn cloth, balls stuck in pocket, etc.
    </p>
    <p>
        <strong>10.13 Resolving Disputes</strong>
        Any disagreement between the two players will be resolved by the League Operator, his/her appointed representative, or any administrative member of the UPA.
    </p>
    <p>
        <strong>11.0 SPECIAL RULINGS</strong>
        Any rule or situation not covered in this text shall be decided in an expedient manner by the League Operator, his/her appointed representative or any administrative member of the UPA in accordance with UPA ideals and guidelines for the purposes of league play to continue. Such expedient rulings shall then be made known to the administration of the UPA Corporate Office and it’s Touring Professionals before further instruction or implementation of the matter is finalized.
    </p>
    <p>
        <strong>11.1 Bylaws</strong>
        These rules are consistent nationwide and are not to be altered in any way, shape, or form. The implementation of any “Bylaws,” or any other document, is strictly prohibited by UPA Corporate Office.
    </p>
    <p>
        <strong>11.2 Addendums</strong>
        From time to time there may be addendums issued by the UPA to the Official UPA Rulebook, and it is each individual player’s responsibility to keep current.
    </p>
